package String;

public class Printing {

	public static void main(String[] args) {
		 
		int x=10 , y=20;
		
		System.out.println(x+y+" sum"); //here first add and then concatenate to string
		
		System.out.println("sum "+x+y); // both plus is use for concatenation 
		
		System.out.println("sum "+ (x+y)); // bracket is use for do first sum operation and concatenate to string
		
		System.out.println("sum of "+x +" and "+y+" is "+(x+y));

	}

}
